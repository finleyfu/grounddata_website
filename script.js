function setLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang] && i18n[lang][key]) {
            el.textContent = i18n[lang][key];
        }
    });
    document.documentElement.lang = lang;
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    localStorage.setItem('preferredLanguage', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage');
    const browserLang = navigator.language || navigator.userLanguage;
    const initialLang = savedLang || (browserLang.startsWith('zh') ? 'zh' : 'en');
    setLanguage(initialLang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.dataset.lang);
        });
    });

    const navbar = document.querySelector('.navbar');
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const navObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (!entry.isIntersecting) {
                navbar.classList.add('scrolled');
            } else {
                navbar.classList.remove('scrolled');
            }
        });
    }, observerOptions);

    const heroSection = document.querySelector('.hero');
    if (heroSection) {
        navObserver.observe(heroSection);
    }

    const elementObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                const delay = entry.target.dataset.delay || 0;
                setTimeout(() => {
                    entry.target.classList.add('animate-in');
                }, delay);
                elementObserver.unobserve(entry.target);
            }
        });
    }, observerOptions);

    const featureCards = document.querySelectorAll('.feature-card');
    featureCards.forEach((card, index) => {
        card.dataset.delay = index * 100;
        elementObserver.observe(card);
    });

    const valueItems = document.querySelectorAll('.value-item');
    valueItems.forEach((item, index) => {
        item.dataset.delay = index * 80;
        elementObserver.observe(item);
    });

    const valuesMain = document.querySelector('.values-main');
    if (valuesMain) {
        valuesMain.dataset.delay = 0;
        elementObserver.observe(valuesMain);
    }

    const archLayers = document.querySelectorAll('.arch-layer');
    archLayers.forEach((layer, index) => {
        layer.dataset.delay = index * 150;
        elementObserver.observe(layer);
    });

    const archConnectors = document.querySelectorAll('.arch-connector');
    archConnectors.forEach((connector, index) => {
        connector.dataset.delay = index * 150 + 75;
        elementObserver.observe(connector);
    });

    const testimonialCards = document.querySelectorAll('.testimonial-card');
    testimonialCards.forEach((card, index) => {
        card.dataset.delay = index * 150;
        elementObserver.observe(card);
    });

    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            const target = document.querySelector(targetId);
            if (target) {
                const navbarHeight = navbar.offsetHeight;
                const targetPosition = target.getBoundingClientRect().top + window.pageYOffset - navbarHeight - 20;
                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    let ticking = false;
    window.addEventListener('mousemove', (e) => {
        if (!ticking) {
            window.requestAnimationFrame(() => {
                const orb = document.querySelector('.orb');
                if (orb) {
                    const rect = orb.getBoundingClientRect();
                    const centerX = rect.left + rect.width / 2;
                    const centerY = rect.top + rect.height / 2;
                    const x = (centerX - e.clientX) / 60;
                    const y = (centerY - e.clientY) / 60;
                    orb.style.transform = `translate(${x}px, ${y}px)`;
                }
                ticking = false;
            });
            ticking = true;
        }
    });

    const footerYear = document.querySelector('.footer-bottom p');
    if (footerYear) {
        const currentYear = new Date().getFullYear();
        footerYear.innerHTML = footerYear.innerHTML.replace('2026', currentYear);
    }
});